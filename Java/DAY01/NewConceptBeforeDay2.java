package JAVA.DAY01;

public class NewConceptBeforeDay2 {
    public static void main(String[]args){
        int x=5;
        int y=4;
        y--;
        x++;
        System.out.println(x);
        System.out.println(y);


        int a = 10;
        a++;

        System.out.println(a);


        int b = 10;
        b--;

        System.out.println(b);
    }



}


/* New Concept Before Day 2
Increment Operator
int x = 5;

x++;

System.out.println(x);

Output:

6

Means:

x = x + 1;
Decrement Operator
int x = 5;

x--;

System.out.println(x);

Output:

4

Means:

x = x - 1;*/